# A to Z, 0 to 9)
# 2. isalpha() : return True if characters are only alphabet symbols(a to z,
# A to Z)
# 3. isdigit() : return True if characters are only digits (0 to 9)
# 4. islower() : return True if characters are in lower case alphabet
# 5. isupper() : return True if characters are in upper case alphabet
# 6. istitle() : return True if characters are in title case alphabet
# 7. isspace() : return True if string contain only spaces

