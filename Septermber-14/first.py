# Iterative Statement 
# If we want to execute a group of statements multiple times then we should 
# go for iterative statement 

# Python supports 2 types of iterative statement 
# 1. for loop
# 2. while loop

# 1. For Loop : If we want to execute some action for every element present in 
# some sequence (It may be a string or collection ) then we should go for for loop

# print character from string 

name = "Amisha"
for character in name:
    print(character)



